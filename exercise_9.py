import numpy as np


def multiply_two_arrays(first_array: np.ndarray, second_array: np.ndarray) -> np.ndarray:
    """
    Multiple two arrays and return its result
    :param first_array: First array to combine
    :param second_array:  second array to combine
    :return: Result array after multiplication
    """
    return np.multiply(first_array, second_array)
